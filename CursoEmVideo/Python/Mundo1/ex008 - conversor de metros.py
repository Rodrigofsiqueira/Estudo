metros = int(input('Digite um valor em metros: '))

print('O valor de {} metros em'.format(metros))
print('Kilometros é {}'.format(metros*0.0001))
print('Hectometros é {}'.format(metros*0.001))
print('Decametros é {}'.format(metros*0.01))
print('Decimetros é {}'.format(metros*10))
print('Centimetros é {}'.format(metros*100))
print('Milimetros é {}'.format(metros*1000))
